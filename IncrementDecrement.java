public class IncrementDecrement {
    public static void main(String[] args) {
        int a=10;

        System.out.println(a++);

        System.out.println(++a);

        int b = 69;

        System.out.println(b--);

        System.out.println(--b);
        /* note -> a++ will print & then update the value in a
                   ++a will update the value then print it */

    }
}
